<h2 id="article1">ARTICLE 1 : <strong>DEFINITIONS</strong></h2>
<p>
    <strong>"Clienti"</strong>: indica qualsiasi professionista, ai sensi del Codice di Commercio,
    che utilizza il <strong>Sito</strong> o l’<strong>Applicazione</strong> per ordinare dati
    da noi raccolti ed elaborati e, se del caso, studi di marketing con o senza analisi.
</p>
<p>
    <strong>"ClicWalker"</strong>: indica qualsiasi persona fisica nella sua qualità di un privato iscritta al Sito o all'Applicazione
    Clic and Walk e che intende aderire alla community dei <strong>ClicWalkers</strong>, ai fini di raccogliere e di
    trasmettere <strong>informazioni</strong> e dati nell'ambito delle <strong>Missioni</strong> definite di seguito.
</p>
<p>
    <strong>"Clic and Walk"</strong>: indica la società editrice del Sito e dell'Applicazione
    mobile associata Clic and Walk.
</p>
<p>
    <strong>"Missione(i)"</strong>: indica la raccolta di dati, quali fotografie, sequenze
    video e/o audio, punti di geolocalizzazione, realizzati dai <strong>ClicWalkers</strong> e trasmessi
    a <strong>Clic and Walk</strong> e, se del caso, le risposte ai questionari indirizzati ai <strong>ClicWalkers</strong>
    sotto forma di autosondaggi. Le offerte di <strong>Missioni</strong> riportano nel dettaglio le specifiche richieste
    per lo svolgimento di ciascuna Missione.
</p>
<p>
    <strong>"Elemento(i)"</strong>: indica le fotografie, le sequenze video e/o audio,
    i dati di geolocalizzazione raccolti e forniti dai <strong>ClicWalkers</strong> alla società <strong>Clic and Walk</strong>.
</p>
<p>
    <strong>"Informazioni"</strong>: indica i commenti e le risposte forniti dai <strong>ClicWalkers</strong>
    in risposta ai questionari prestabiliti dalla società <strong>Clic and Walk</strong> nell'ambito di autosondaggi.
</p>
<p>
    <strong>"Sito(i)"</strong>: indica il Sito pubblicato dalla società <strong>Clic and Walk</strong> accessibile
    all'indirizzo seguente: www.clicandwalk.com. Il Sito è accessibile a tutti mentre l'accesso
    personale è riservato ai <strong>ClicWalkers</strong> che dispongono di un account, tramite il quale
    accetteranno le presenti Condizioni Generali di Utilizzo.
</p>
<p>
    <strong>"Applicazione"</strong>: indica l'Applicazione pubblicata dalla società <strong>Clic and Walk</strong>
    disponibile gratuitamente per il download, in particolare su Google Play e l'Apple Store.
</p>
<p>
    <strong>"Terminale Mobile"</strong>: indica in particolare smartphone, attrezzature
    informatiche e tavolette digitali.
</p>
<p>
    <strong>"Forza Maggiore"</strong> : indica qualsiasi evento indipendente dal controllo
    delle parti tra cui, ma non solo, calamità naturali, guerre, vertenze collettive delle
    maestranze, manifestazioni, incendi, inondazioni, nubifragi, tempeste, esplosioni, rispetto
    di obblighi giuridici, atti o omissioni del governo o delle autorità di regolamentazione,
    atti di terrorismo ed emergenze nazionali. Costituiscono altresì casi di forza maggiore
    i guasti o le interruzioni dell'alimentazione elettrica, le inadempienze di un fornitore
    o di operatori e/o provider di telecomunicazioni terzi.
</p>
