    funcionalidades y modalidades de utilización de la <strong>Aplicación</strong> y de la <strong>Página Web</strong>.
    La <strong>Página Web</strong> constituye una plataforma que permite a los <strong>Clientes</strong> solicitar colectas
    de datos y, llegado el caso, estudios de márquetin que serán el objeto de las <strong>Misiones</strong>
    propuestas por la <strong>Aplicación</strong> a los ClickWalkers. <strong>Clic and Walk</strong> pone a disposición de los
    internautas, asimismo, un cierto número de <strong>Información</strong> y consejos prácticos que se comunican
    a título indicativo y de las que <strong>Clic and Walk</strong> no se hace responsable.
</p>
