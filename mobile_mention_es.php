<h2 id="article1">ARTÍCULO 1 : <strong>DEFINICIONES</strong></h2>
<p>
    <strong>"Clientes"</strong>: «Clientes»: designa a todo profesional,
    tal y como se define en el Código de comercio francés, que utiliza
    la Página Web o la Aplicación con el fin de solicitar datos recogidos y
    tratados por nosotros y, llegado el caso, estudios de márquetin acompañados
    o no de análisis.
</p>
<p>
    <strong>"ClicWalker"</strong>: designa a toda persona física en su calidad de un particular inscrita en la
    Página Web o en la Aplicación <strong>Clic and Walk</strong>, y que desea integrar la comunidad
    de <strong>ClicWalkers</strong> con vistas a recopilar y transmitir <strong>Información</strong> y datos en el
    marco de las <strong>Misiones</strong> definidas más adelante.
</p>
<p>
    <strong>"Clic and Walk"</strong>: designa a la sociedad editora de la Página
    Web y de la Aplicación móvil asociada <strong>Clic and Walk</strong>.
</p>
<p>
    <strong>"Misión o misiones"</strong>: designa la colecta de datos, tales como
    fotografías, secuencias de vídeo y/o sonoras o puntos de geolocalización,
    realizada por los <strong>ClicWalkers</strong> y transmitidos a <strong>Clic and Walk</strong> y, llegado el caso,
    las respuestas de los cuestionarios dirigidos a los <strong>ClicWalkers</strong> en forma de encuestas
    autoadministradas. Las ofertas de <strong>Misiones</strong> precisan las especificaciones requeridas
    para la realización de cada <strong>Misión</strong>.
</p>
<p>
    <strong>"Elemento(s)"</strong>: designa las fotografías, secuencias de vídeo y/o sonoras,
    los datos de geolocalización recopilados y proporcionados por los <strong>ClicWalkers</strong> a la sociedad
    <strong>Clic and Walk</strong>.
</p>

<p>
    <strong>"Información"</strong>: designa los comentarios y respuestas proporcionados por los
    <strong>ClicWalkers</strong> en respuesta a los cuestionarios preestablecidos por la sociedad <strong>Clic and Walk</strong>
    en el marco de encuestas autoadministradas.
</p>
<p>
    <strong>"Página(s) Web"</strong>: designa la <strong>Página Web</strong> editada por la sociedad <strong>Clic and Walk</strong>
    accesible desde la siguiente dirección: www.clicandwalk.com. La <strong>Página Web</strong> es accesible a todos
    y el acceso personal está reservado a los <strong>ClicWalkers</strong> que dispongan de una cuenta por medio de
    la cual estos últimos aceptarán las presentes Condiciones Generales de Uso.
</p>
<p>
    <strong>"Aplicación"</strong>: designa la Aplicación editada por la sociedad <strong>Clic and Walk</strong>
    disponible para descargar de manera gratuita desde Google Play y la Apple Store.
</p>
<p>
    <strong>"Terminal Móvil”</strong>: designa en particular todos los Smartphone, equipos
    informáticos y tabletas digitales.
</p>
<p>
    <strong>"Fuerza Mayor"</strong> : todo evento que escape del control de las Partes,
    en particular, pero no limitándose a los casos de catástrofe natural, guerra, conflicto
    laboral colectivo, manifestación, incendio, inundación, tormenta, tempestad, explosión,
    respeto de las obligaciones legales, acciones u omisiones del gobierno o de las autoridades
    reglamentarias, actos terroristas y urgencias nacionales. Constituyen, asimismo, casos de
    fuerza mayor los fallos o interrupciones de la alimentación eléctrica, el fallo de un
    proveedor o bien de operadores y/o proveedores de telecomunicaciones terceros.
</p>
