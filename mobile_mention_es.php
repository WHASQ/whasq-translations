<h2 id="article4">ARTÍCULO 4: FUNCIONAMIENTO DE LAS MISIONES</h2>

<p>
    El <strong>ClicWalker</strong> queda informado de que es totalmente libre para responder o no a una <strong>Misión</strong> propuesta por <strong>Clic and Walk</strong>.
</p>
<p>
    En caso de aceptación, gestionará la realización de la <strong>Misión</strong> en función de sus horarios, quedando precisado que
    puede abandonarla en todo momento.
</p>
<p>
    Tras la inscripción en la <strong>Página Web</strong> y la <strong>Aplicación</strong>, se avisará al <strong>ClicWalker</strong> de las <strong>Misiones</strong>
    disponibles en el perímetro en el cual se encuentra, bien sea por notificación o consultando
    la lista de <strong>Misiones</strong> disponibles en la <strong>Aplicación</strong>.
</p>
<p>
    Se invita al <strong>ClicWalker</strong> a leer con atención el título de la <strong>Misión</strong>, con el objetivo de conocer
    las eventuales especificaciones técnicas necesarias para aceptar la <strong>Misión</strong>.
</p>
<p>
    Una vez aceptada la <strong>Misión</strong>, esta se retira de la lista de ofertas de <strong>Misiones</strong> disponibles para
    los demás <strong>ClicWalkers</strong>, quedando precisado que el <strong>ClicWalker</strong> solo está autorizado a realizar
    una <strong>Misión</strong> a la vez.
</p>
<p>
    El <strong>ClicWalker</strong> queda informado de que deberá adaptarse a los reglamentos internos de los
    establecimientos visitados.
</p>
<p>
    La transmisión de los <strong>Elementos</strong> y la <strong>Información</strong> se hará exclusivamente
    (salvo aviso contrario por parte de <strong>Clic and Walk</strong>) a partir del Terminal
    Móvil del <strong>ClicWalker</strong>. Una vez que los <strong>Elementos</strong> y la <strong>Información</strong> hayan sido
    transmitidos en el plazo establecido, el <strong>ClicWalker</strong> recibirá un acuse de recibo en
    forma de notificación si este las ha aceptado, y queda advertido que <strong>Clic and Walk</strong>
    dispone de un plazo de 7 días laborables para verificar la conformidad de los <strong>Elementos</strong>
    transmitidos con las especificaciones técnicas de la oferta de <strong>Misión</strong>.
</p>
<p>
    Al cumplirse el plazo, si la <strong>Misión</strong> se ha ejecutado conforme a las especificaciones
    técnicas de la oferta de <strong>Misión</strong>, se abonará en la cuenta del <strong>ClicWalker</strong> el importe anunciado en la oferta.
</p>
<p>
    En caso de rechazo de los <strong>Elementos</strong> transmitidos por el <strong>ClicWalker</strong>, se informará a este
    último de las razones que han motivado dicho rechazo por correo electrónico a la dirección
    de e-mail indicada en la inscripción. La <strong>Misión</strong> desaparecerá de la cuenta del <strong>ClicWalker</strong> y
    estará de nuevo accesible para el conjunto de la comunidad de <strong>ClicWalkers</strong>.
</p>
<p>
    En caso de fraude o de transmisión de contenidos contrarios a las buenas costumbres,
    podrá excluirse al <strong>ClicWalker</strong> de la comunidad de <strong>ClicWalkers</strong> mediante el cierre
    automático de su cuenta.
</p>
<p>
    En todos los casos, el <strong>ClicWalker</strong> en su calidad de un particular, no está sometido a ninguna relación de subordinación.
</p>
<p>
    El <strong>ClicWalker</strong> reconoce con las presentes condiciones que actúa en calidad de prestatario
    de servicios libre y autónomo en la ejecución de sus <strong>Misiones</strong>, que realiza de manera ocasional.
</p>
<p>
    Por cada <strong>Misión</strong> realizada y tras la verificación de los <strong>Elementos</strong> transmitidos, en <strong>aplicación</strong>
    del artículo 5 de las presentes, <strong>Clic and Walk</strong> abonará en la cuenta virtual del <strong>ClicWalker</strong> el
    importe indicado en la oferta de <strong>Misión</strong> inmediatamente, a partir de la validación de la <strong>misión</strong>.
    La <strong>misión</strong> aparecerá entonces como validada en la <strong>aplicación</strong>.
</p>
<p>
    Cuando el <strong>ClicWalker</strong> haya alcanzado el umbral a partir del cual puede solicitar el pago de sus
    créditos, <strong>Clic and Walk</strong> se obligará a efectuar dicho pago según las modalidades indicadas en el
    artículo 8 de las presentes.
</p>
<h2 id="article5"> ARTÍCULO 5: ESPECIFICACIONES TÉCNICAS</h2>
<p>
    A título de las «especificaciones técnicas» que figuran en las ofertas de <strong>Misiones</strong>,
    el <strong>ClicWalker</strong> podrá conocer el contenido y las características de las <strong>Misiones</strong> de
    la siguiente manera:
</p>
<ul>
    <li>
        - Tomar la fotografía del producto «X» en primer plano, o de la sección «Y»,
        de las etiquetas de precio de los productos «X» o grabar un lugar…
    </li>
    <li>
        - <strong>Misión</strong> que debe efectuarse tal día, en tal tienda «Y» …
    </li>
    <li>
        - Tomar una fotografía de su propio salón…
    </li>
</ul>
<p>
    Queda convenido entre las Partes que las especificaciones técnicas confieren a
    las <strong>Misiones</strong> un carácter simple y elemental y que no requieren ninguna
    competencia o habilidad específica.
</p>
<p>
    Al final de las <strong>Misiones</strong>, los <strong>Elementos</strong> recogidos y proporcionados por el <strong>ClicWalker</strong>
    serán apreciados objetivamente por <strong>Clic and Walk</strong> siguiendo los siguientes criterios:
</p>
<ul>
    <li>
        (i) (i) las fotos o vídeos deben ser visibles, de buena calidad y/o audibles,
    </li>
    <li>
        (ii) (ii) las fotos o vídeos deben estar bien encuadradas y respetar el contenido
        y el objeto de las <strong>Misiones</strong>,
    </li>
    <li>
        (iii) (iii) las fotos o vídeos no deben incluir la presencia de terceros,
        individuos (mayores o menores de edad) o animales,
    </li>
    <li>
        (iv) (iv) en caso de encuestas autoadministradas, la <strong>Información</strong> transmitida
        debe ser completa y exhaustiva (cuestionarios, comentarios, notas…).
    </li>
</ul>
