    verbunden sind.
</p>
<p>
    Nach Annahme des <strong>Auftrags</strong> wird dieser von der Liste der verfügbaren Aufträge, die den anderen
    <strong>ClicWalker</strong> angeboten werden, entfernt, wobei darauf hingewiesen wird, dass der ClicWalker nur
    befugt ist, jeweils einen <strong>Auftrag</strong> gleichzeitig auszuführen.
</p>
<p>
    Dem <strong>ClicWalker</strong> wird mitgeteilt, dass er die Geschäftsordnung der aufgesuchten Unternehmen zu
    befolgen hat.
</p>
<p>
